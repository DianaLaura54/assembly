DATA SEGMENT PARA PUBLIC 'DATA'
	ARRAY DB 1,2,3,4,8,7,5,1
	LEN DB $-ARRAY
	MESSAGE DB 'The sum is: ','$'
	SUM DB 2 DUP(?)
	RESULT DB 2 DUP(?),'$'
DATA ENDS

CODE SEGMENT PARA PUBLIC 'CODE'
ASSUME DS:DATA,CS:CODE
START PROC FAR
	
	PUSH DS
	XOR AX,AX
	PUSH AX
	MOV AX,DATA
	MOV DS,AX
	
	XOR CX,CX
	MOV CL,LEN
	
	MOV SUM,0
	
	MOV SI,0
	MOV AX,0
	
	
	;calculate the sum
calculateSum:
	MOV AL,ARRAY[SI]
	ADD SUM,AL
	INC SI
	LOOP calculateSum
	
	;print the message
	MOV AH,09H
	MOV DX, OFFSET MESSAGE
	INT 21H
	
	;PREPARE THE RESULT TO BE PRINTED
	MOV BL,10
	MOV AL,SUM
	MOV AH,0
	MOV DI,1

displayResult:
	DIV BL
	MOV RESULT[DI],AH
	ADD RESULT[DI],'0'
	DEC DI
	MOV AH,0
	CMP AL,0
	JNE displayResult
	
	;print thE RESULT
	MOV AH,09H
	MOV DX, OFFSET RESULT
	INT 21H
	
	MOV AH, 4CH
	INT 21H
	
START ENDP
CODE ENDS
END START